export function translate(
  locale: Locale,
  key: TranslationKey,
  params?: Record<string, string | number>
): string {
  let str = T[locale]?.[key] ?? T.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replaceAll(`{${k}}`, String(v));
    }
  }
  return str;
}

let current: Locale = "en";
const subs = new Set<(l: Locale) => void>();

// Migrate a stored locale from the legacy "pt" tag to "pt-BR" — production
// users who picked Portuguese before the rename should not silently fall
// back to English.
function normalizeStored(raw: string | null | undefined): Locale | null {
  if (!raw) return null;
  if (raw === "pt") return "pt-BR";
  return (SUPPORTED as string[]).includes(raw) ? (raw as Locale) : null;
}

function detectFromBrowser(): Locale {
  if (typeof navigator === "undefined") return "en";
  const raw = (navigator.language || "en").toLowerCase();
  // Match the most-specific tag we ship first, then fall back to the
  // primary subtag.
  if (raw.startsWith("pt")) return "pt-BR";
  if (raw.startsWith("ja")) return "ja";
  if (raw.startsWith("hi")) return "hi";
  const primary = raw.slice(0, 2);
  return (SUPPORTED as string[]).includes(primary) ? (primary as Locale) : "en";
}

// /nl/sudoku → "nl", /pt-BR/sudoku → "pt-BR", /sudoku → null.
// Returning null lets later detection steps (localStorage, browser)
// take over for the flat (English-canonical) routes.
function detectFromPath(): Locale | null {
  if (typeof window === "undefined") return null;
  const seg = window.location.pathname.split("/")[1] ?? "";
  if (!seg) return null;
  return (SUPPORTED as string[]).includes(seg) ? (seg as Locale) : null;
}

function setLocale(l: Locale) {
  current = l;
  if (typeof window !== "undefined") {
    try { localStorage.setItem(STORAGE_KEY, l); } catch {}
    document.documentElement.setAttribute("lang", l);
  }
  subs.forEach((fn) => fn(l));
}

export function useLocale() {
  const [locale, set] = useState<Locale>(current);

  useEffect(() => {
    // Priority order: URL path > stored preference > browser language.
    // The URL wins so that landing on /nl/sudoku from a Google result
    // actually renders Dutch UI even if the visitor previously picked
    // another language. Without this, the SSR'd <title> would say
    // "Sudoku" (NL hreflang cluster) while the visible UI showed
    // English — a content mismatch that crawlers penalise.
    const fromPath = detectFromPath();
    const stored = typeof window !== "undefined"
      ? normalizeStored(localStorage.getItem(STORAGE_KEY))
      : null;
    const initial: Locale = fromPath ?? stored ?? detectFromBrowser();
    if (initial !== current) {
      current = initial;
      document.documentElement.setAttribute("lang", initial);
      // Persist normalised value so the legacy "pt" entry is rewritten.
      if (typeof window !== "undefined") {
        try { localStorage.setItem(STORAGE_KEY, initial); } catch {}
      }
      set(initial);
    } else {
      document.documentElement.setAttribute("lang", current);
    }
    const fn = (l: Locale) => set(l);
    subs.add(fn);
    return () => { subs.delete(fn); };
  }, []);

  const change = useCallback((l: Locale) => setLocale(l), []);
  const t = useCallback(
    (key: TranslationKey, params?: Record<string, string | number>) => translate(locale, key, params),
    [locale]
  );
  return { locale, setLocale: change, t };
}
